class GameDetails {
  final int id;
  final String title;
  final String description;
  final String price;
  final String genre;
  final String developer;
  final String publisher;
  final String imageUrl;

  GameDetails({
    required this.id,
    required this.title,
    required this.description,
    required this.price,
    required this.genre,
    required this.developer,
    required this.publisher,
    required this.imageUrl,
  });
}
